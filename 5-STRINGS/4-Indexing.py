string= "kyaa re saale kaisa hai "
print(string[5])

#<---SLICING---> of the string
print(string[2:7])
print(string[:9])  #python takes starting value as index-0
print(string[4:])  #python takes ending values as last index value of the string

#<---NEGATIVE SLICING--->
str="KOKILABEHEN"
print(str[-5:-2])
print(str[-5:])  #python takes ending values as last index value of the string
print(str[-11:-5])